-- Tables
--

CREATE TABLE "Application" (
	"ApplicationID" int NOT NULL,
	"ApplicationName" varchar(1024) NOT NULL,
	"ApplicationEnabled" boolean NOT NULL,
	CONSTRAINT "PK_Application" PRIMARY KEY ("ApplicationID"),
	CONSTRAINT "IX_Application_ApplicationName" UNIQUE ("ApplicationName")
);

CREATE TABLE "ApplicationDependency" (
	"ApplicationID" int NOT NULL,
	"RequiredApplicationID" int NOT NULL,
	"MinimumVersion" varchar(64) NULL,
	CONSTRAINT "PK_ApplicationDependency" PRIMARY KEY ("ApplicationID", "RequiredApplicationID"),
	CONSTRAINT "FK_ApplicationDependency_Application_ApplicationID" FOREIGN KEY ("ApplicationID") REFERENCES "Application" ("ApplicationID") ON UPDATE CASCADE,
	CONSTRAINT "FK_ApplicationDependency_Application_RequiredApplicationID" FOREIGN KEY ("RequiredApplicationID") REFERENCES "Application" ("ApplicationID") ON UPDATE CASCADE
);

CREATE TABLE "Action" (
	"ActionID" int NOT NULL,
	"ActionType" int NOT NULL,
	"ActionParameters" varchar(1024) NOT NULL,
	"ActionArguments" varchar(1024) NULL,
	CONSTRAINT "PK_Action" PRIMARY KEY ("ActionID")
);

CREATE TABLE "Release" (
	"ReleaseID" int NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"ServiceDate" timestamp with time zone NOT NULL,
	"ApplicationID" int NOT NULL,
	"Package" varchar(1024) NOT NULL,
	"PackageHash" varchar(64) NULL,
	"Version" varchar(64) NOT NULL,
	"ActionID" int NOT NULL,
	"ReleaseEnabled" boolean NOT NULL,
	"AllowConcurrency" boolean NOT NULL,
	CONSTRAINT "PK_Release" PRIMARY KEY ("ReleaseID"),
	CONSTRAINT "FK_Release_Application" FOREIGN KEY ("ApplicationID") REFERENCES "Application" ("ApplicationID") ON UPDATE CASCADE,
	CONSTRAINT "FK_Release_Action" FOREIGN KEY ("ActionID") REFERENCES "Action" ("ActionID") ON UPDATE CASCADE
);

CREATE TABLE "Tenant" (
	"TenantID" int4 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"TenantName" varchar(128) NOT NULL,
	"Email" varchar(128) NOT NULL,
	"TenantEnabled" boolean NOT NULL,
	CONSTRAINT "PK_Tenant" PRIMARY KEY ("TenantID"),
	CONSTRAINT "IX_Tenant_TenantName" UNIQUE ("TenantName")
);

CREATE TABLE "Device" (
	"DeviceID" int NOT NULL GENERATED BY DEFAULT AS IDENTITY,	
	"DeviceToken" varchar(64) NOT NULL,
	"DeviceName" varchar(1024) NOT NULL,
	"DeviceLocation" varchar(1024) NOT NULL,
	"DeviceEnabled" boolean NOT NULL,
	CONSTRAINT "PK_Device" PRIMARY KEY ("DeviceID"),
	CONSTRAINT "IX_Device_DeviceName" UNIQUE ("DeviceName"),
	CONSTRAINT "IX_Device_DeviceToken" UNIQUE ("DeviceToken")
);

CREATE TABLE "DeviceFingerprint" (
	"FingerprintType" int NOT NULL,
	"FingerprintValue" varchar(1024) NOT NULL,
	"DeviceID" int NOT NULL,
	CONSTRAINT "PK_DeviceFingerprint" PRIMARY KEY ("FingerprintType", "FingerprintValue"),
	CONSTRAINT "FK_DeviceFingerprint_Device" FOREIGN KEY ("DeviceID") REFERENCES "Device" ("DeviceID") ON UPDATE CASCADE
);

CREATE TABLE "DeviceMetric" (
	"DeviceID" int NOT NULL,
	"ServiceDate" timestamp with time zone NOT NULL,
	"DeviceDate" timestamp with time zone NOT NULL,
	"LastReboot" timestamp with time zone NOT NULL,
	"KernelVersion" varchar(1024) NOT NULL,
	"CpuUser" numeric NOT NULL,
	"CpuSystem" numeric NOT NULL,
	"CpuIdle" numeric NOT NULL,
	"CpuTemperature" numeric NOT NULL,
	"MemoryTotal" int NOT NULL,
	"MemoryUsed" int NOT NULL,
	"MemoryFree" int NOT NULL,
	"DiskTotal" int NOT NULL,
	"DiskUsed" int NOT NULL,
	"DiskFree" int NOT NULL,
	CONSTRAINT "PK_DeviceMetric" PRIMARY KEY ("DeviceID", "ServiceDate"),
	CONSTRAINT "FK_DeviceMetric_Device" FOREIGN KEY ("DeviceID") REFERENCES "Device" ("DeviceID") ON UPDATE CASCADE
);

CREATE TABLE "DeviceApplication" (
	"DeviceID" int NOT NULL,
	"ApplicationID" int NOT NULL,
	"DeviceApplicationEnabled" boolean NOT NULL,	
	CONSTRAINT "PK_DeviceApplication" PRIMARY KEY ("DeviceID", "ApplicationID"),
	CONSTRAINT "FK_DeviceApplication_Device" FOREIGN KEY ("DeviceID") REFERENCES "Device" ("DeviceID") ON UPDATE CASCADE,
	CONSTRAINT "FK_DeviceApplication_Application" FOREIGN KEY ("ApplicationID") REFERENCES "Application" ("ApplicationID") ON UPDATE CASCADE
);

CREATE TABLE "DeviceDeployment" (
	"DeploymentID" int NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"DeviceID" int NOT NULL,
	"ReleaseID" int NOT NULL,
	"DeviceDate" timestamp with time zone NOT NULL,
	"Success" boolean NOT NULL,
	"Details" text NULL,
	CONSTRAINT "PK_DeviceDeployment" PRIMARY KEY ("DeploymentID"),
	CONSTRAINT "IX_DeviceDeployment_DeviceIDReleaseID" UNIQUE ("DeviceID", "ReleaseID"),
	CONSTRAINT "FK_DeviceDeployment_Device" FOREIGN KEY ("DeviceID") REFERENCES "Device" ("DeviceID") ON UPDATE CASCADE,
	CONSTRAINT "FK_DeviceDeployment_Release" FOREIGN KEY ("ReleaseID") REFERENCES "Release" ("ReleaseID") ON UPDATE CASCADE
);

CREATE TABLE "DeviceTenant" (
	"DeviceID" int NOT NULL,
	"TenantID" int NOT NULL,
	CONSTRAINT "PK_DeviceTenant" PRIMARY KEY ("DeviceID", "TenantID"),
	CONSTRAINT "FK_DeviceTenant_Device" FOREIGN KEY ("DeviceID") REFERENCES "Device" ("DeviceID") ON UPDATE CASCADE,
	CONSTRAINT "FK_DeviceTenant_Tenant" FOREIGN KEY ("TenantID") REFERENCES "Tenant" ("TenantID") ON UPDATE CASCADE
);

CREATE TABLE "User" (
	"UserID" int4 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"TenantID" int4 NOT NULL,
	"Username" varchar(128) NOT NULL,
	"Password" varchar(128) NOT NULL,
	"FullName" varchar(128) NOT NULL,
	"Email" varchar(128) NOT NULL,
	"Roles" varchar(1024) NOT NULL,
	"UserEnabled" boolean NOT NULL,
	CONSTRAINT "PK_User" PRIMARY KEY ("UserID"),
	CONSTRAINT "IX_User_Username" UNIQUE ("Username"),
	CONSTRAINT "FK_User_Tenant" FOREIGN KEY ("TenantID") REFERENCES "Tenant" ("TenantID") ON UPDATE CASCADE
);